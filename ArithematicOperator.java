public class ArithematicOperator {
  public static void main(String[] args) {
    int a=10;
    int b=20;
    System.out.println(a+b);
    System.out.println(a-b);
    System.out.println(a*b);
    System.out.println(a/b);
    System.out.println(a%b);
    System.out.println();
    System.out.println();
    double x=10;
    double y=20;
    System.out.println(x+y);
    System.out.println(x-y);
    System.out.println(x*y);
    System.out.println(x/y);
    System.out.println(x%y);
  }
}
